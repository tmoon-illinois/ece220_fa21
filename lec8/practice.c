#include <stdio.h>



int main(){
    int a = 6, b = 9;


    printf("b / a = %f\n", (float) b/a);
    printf("a = b  => %d\n", a = b);
    printf("a = %d, b = %d\n", a, b);
    printf("a = b = 5 => %d\n", a= b = 5);
    printf("++a + b-- = %d\n", ++a + b--);
    
    printf("a = %d, b = %d\n", a, b);


    return 0;
}
